<template>
  <div class="app-container">
    <el-row :gutter="20">
      <el-col :span="6" :xs="24">
        <el-card class="box-card">
          <div slot="header" class="clearfix">
            <span>个人信息</span>
          </div>
          <div>
            <div class="text-center">
              <userAvatar />
            </div>
            <ul class="list-group list-group-striped">
              <li class="list-group-item">
                <svg-icon icon-class="user" style="margin-right: 5px" />用户名称
                <div class="pull-right">{{ user.userName }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="phone" style="margin-right: 5px" />手机号码
                <div class="pull-right">{{ user.phoneNumber }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="email" style="margin-right: 5px" />用户邮箱
                <div class="pull-right">{{ user.email }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="peoples" style="margin-right: 5px" />所属角色
                <div class="pull-right">{{ roleGroup }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="logininfor" style="margin-right: 5px" />登录地址
                <div class="pull-right">{{ user.loginIp }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="date" style="margin-right: 5px" />创建日期
                <div class="pull-right">{{ user.createTime }}</div>
              </li>
              <li class="list-group-item">
                <svg-icon icon-class="example" style="margin-right: 5px" />登录时间
                <div class="pull-right">{{ user.loginDate }}</div>
              </li>
            </ul>
          </div>
        </el-card>
      </el-col>
      <el-col :span="18" :xs="24">
        <el-card>
          <div slot="header" class="clearfix">
            <span>基本资料</span>
          </div>
          <el-tabs v-model="activeTab">
            <el-tab-pane label="基本资料" name="userinfo">
              <userInfo :user="userInfo" @getUser="getUser" />
            </el-tab-pane>
            <el-tab-pane label="修改密码" name="resetPwd">
              <resetPwd :user="user" />
            </el-tab-pane>
          </el-tabs>
        </el-card>
      </el-col>
    </el-row>
  </div>
</template>

<script>
import { getInfo } from '@/api/user'
import userInfo from './userInfo'
import resetPwd from './resetPwd'
import userAvatar from './userAvatar'

export default {
  name: 'Profile',
  components: { userInfo, resetPwd, userAvatar },
  data() {
    return {
      user: {},
      userInfo: {},
      roleGroup: '',
      activeTab: 'userinfo'
    }
  },
  created() {
    this.getUser()
  },
  methods: {
    getUser() {
      getInfo().then(response => {
        this.user = response.data
        this.userInfo = JSON.parse(JSON.stringify(this.user))
        this.roleGroup = response.data.rolesName.join(',')
      })
    }
  }
}
</script>
